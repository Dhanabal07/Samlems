package ems;

public class Users 
{
	String name;
	String username;
	String empid;
	String mobile;
	String mail;
	String department;
	String company;
	
	Users(String name,String username,String empid,String mobile,String mail,String department,String company)
	{
		this.name=name;
		this.username=username;
		this.empid=empid;
		this.mobile=mobile;
		this.mail=mail;
		this.department=department;
		this.company=company;
	}
}